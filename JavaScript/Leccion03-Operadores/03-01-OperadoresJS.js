//Operadores aritmeticos +,-,*,**,/,%,++ (increment),-- (decrement)
    //Suma    
let a=3;
let b=2;
let z= a+b;
console.log("Resultado suma: "+z);
    //resta
x=a-b;
console.log("Resultado resta: "+x);
    //Multiplicación * 
y=a*b;
console.log("Resultado multiplicación: "+y);
    //Exponente **
w=a**b;
console.log("Resultado Exponente: "+ w);
    //División /
t=a/b;
console.log("Resultado división: "+t);
    //Porcentaje %
s=a%b; //residuo de la división
console.log("resultado procentaje: "+s);
    //Incremento ++
        //Pre Incremento (el operador ++ antes de la variable) - incremento en 1 el valor de la variable
            r=++a
            console.log("incremento: "+r);
            console.log(a); //se amentó a 4
        //Pos Incremento (el operador ++ después de la variable)
            q=b++;
            console.log("incremento: "+q);
            console.log(b);
            q=b++;
            console.log(b);
            console.log(q);
    //Decremento
            //pre decremento
                p=--a;
                console.log(p);
                console.log(a);
            //pos decremento - se decremeenta la prox vez que se utilice
                o=b--;
                console.log("incremento: "+o);
                console.log(o);
                o=b--;
                console.log(b);
                console.log(o);


